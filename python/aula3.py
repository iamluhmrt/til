# Aspas simples
print('Luiz Martins')

# Aspas dupla
print("Luiz Martins")

# Escape \
print(1, "Luiz \"Henrique\"")  # Não recomendado. (sujo)
print(2, 'Luiz "Martins"') # Recomendado. (limpo)

# r 
print(r"Luiz \"Martins\"") #Mostra o caracter de escape.